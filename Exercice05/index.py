def sum(a, b):
    """
    Additionne deux nombres.

    Cette fonction prend deux arguments, les additionne et retourne le résultat.

    Parameters:
    a (int or float): Le premier nombre à additionner.
    b (int or float): Le deuxième nombre à additionner.

    Returns:
    int or float: La somme de a et b.
    """
    return a + b

def subtraction(a, b):
    """
    Soustrait le deuxième nombre du premier.

    Cette fonction prend deux arguments, soustrait le deuxième du premier et retourne le résultat.

    Parameters:
    a (int or float): Le nombre de départ.
    b (int or float): Le nombre à soustraire de a.

    Returns:
    int or float: La différence entre a et b.
    """
    return a - b
